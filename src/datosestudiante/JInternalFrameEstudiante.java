    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // obtener datos y
        String carnet = this.jTextField1.getText();
        String nombre = this.jTextField2.getText();
        String apellidos = this.jTextField3.getText();
        String carrerA = this.jTextField4.getText();
        String nivel = this.jTextField5.getText();

        try {
            double car = Double.parseDouble(carnet);
            String carrera = null;
            //crear objeto y agregarlo a la lista
            Estudiante estudiante = new Estudiante(car, nombre, apellidos, carrera, nivel);
            this.listaEstudiante.add(estudiante);
            this.limpiarCampos();
            JOptionPane.showMessageDialog(null, "Datos agregados correctamente");
        } catch (Exception e) {
            JOptionPane.showInternalConfirmDialog(null, "Los datos no se han agregado", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

